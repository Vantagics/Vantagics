export function useLanguage() {
    // Detect system language and use it as default
    const getSystemLanguage = (): Language => {
        const browserLang = navigator.language || (navigator as any).userLanguage || '';
        // Check if system language is Chinese (zh, zh-CN, zh-TW, zh-HK, etc.)
        if (browserLang.toLowerCase().startsWith('zh')) {
            return 'ÁÆÄ‰Ωì‰∏≠Êñá';
        }
        // Default to English for all other languages
        return 'English';
    };

    const [language, setLanguage] = useState<Language>(getSystemLanguage());

    const updateLanguage = () => {
        GetConfig().then(config => {
            if (config.language === 'ÁÆÄ‰Ωì‰∏≠Êñá' || config.language === 'English') {
                setLanguage(config.language as Language);
            } else {
                // If config language is not set or invalid, use system language
                setLanguage(getSystemLanguage());
            }
        }).catch(() => {
            // On error, use system language
            setLanguage(getSystemLanguage());
        });
    };

    useEffect(() => {
        updateLanguage();
        // Listen for config changes
        const unsubscribe = EventsOn('config-updated', () => {
            updateLanguage();
        });
        return () => {
            if (unsubscribe) unsubscribe();
        };
    }, []);

    const t = (key: string, ...params: (string | number)[]) => {
        let text = translations[language][key] || key;
        // Replace {0}, {1}, etc. with provided parameters
        params.forEach((param, index) => {
            text = text.replace(`{${index}}`, String(param));
        });
        return text;
    };

    return { language, t };
}
